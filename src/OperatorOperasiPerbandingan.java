public class OperatorOperasiPerbandingan {
    public static void main(String[] args) {

        int a = 100;
        int b = 100;

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);
        // perbandingan bisa dilakukan untuk setiap tipe data number
    }
}
